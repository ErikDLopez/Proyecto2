    public void defineTypeGame() {
        System.out.println("La opcion es "+option);
        
        if (option == 1) {
            typeLabel.setText("UN JUGADOR");
            counterPlayers = 1;
        }
        if (option == 2) {
            typeLabel.setText("DOS JUGADORES");
            counterPlayers = 2;
        }
        counterLabel.setText(String.valueOf(counterPlayers));
    }
    
    public void fillTable(){
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID Jugador","Nombre del Jugador"},MenuCheckersGUI.getUsers().size());
        playersTable.setModel(model);
        
        TableModel modelData = playersTable.getModel();
        
        for (int i = 0; i < MenuCheckersGUI.getUsers().size(); i++) {
            User user = MenuCheckersGUI.getUsers().get(i);
            modelData.setValueAt(user.getID(), i, 0);
            modelData.setValueAt(user.getName(), i, 1);
        }
    }
    
    public void optionsPlayers(int row) {
        if (option == 1) {
                user1 = MenuCheckersGUI.getUsers().get(row);
                user2 = new User("BOT");
                counterPlayers--;
        }
        if (option == 2) {
            switch (counterPlayers) {
                case 2:
                    user1 = MenuCheckersGUI.getUsers().get(row);
                    counterPlayers--;
                    break;
                case 1:
                    user2 = MenuCheckersGUI.getUsers().get(row);
                    counterPlayers--;
                    break;
            }
        }
    }
    
